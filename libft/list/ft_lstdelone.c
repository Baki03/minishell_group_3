#include "../libft.h"

void	ft_lstdelone(t_list *lst, void (*del)(void *))
{
	if (!del)
		return ;
	if (lst)
	{
		del(lst->content);
		lst->content = NULL;
		lst->prev->next = NULL;
		free(lst);
		lst = NULL;
	}
}
